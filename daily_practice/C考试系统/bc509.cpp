/*********************************************************
程序要求：
编写程序，输入两个字符串分别存入两个字符数组中，并比较大小将较大字符串输出。
输入：abcL
      xyL
输出：xy
*********************************************************/
#include <stdio.h>
#include <string.h>
int main()
{
	char a[100],b[100];
	gets(a);
	gets(b);
	if(strcmp(a,b)>0){
		printf("%s",a);
	}
	else{
		printf("%s",b);
	}
}
